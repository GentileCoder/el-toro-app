function t(key) {
  var dict = TRANSLATIONS[currentLang] || TRANSLATIONS.es;
  return dict[key] !== undefined ? dict[key] : (TRANSLATIONS.es[key] || key);
}

function getLangLocale() {
  return {es: 'es-ES', en: 'en-US', de: 'de-DE'}[currentLang] || 'es-ES';
}

function getDayNames() {
  return [t('day.tue'), t('day.wed'), t('day.thu'), t('day.fri'), t('day.sat')];
}

function slotLabel(slot) {
  var key = 'slot.' + slot;
  return t(key) !== key ? t(key) : slot;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });
  document.querySelectorAll('.lang-btn').forEach(function(b) {
    var active = b.getAttribute('data-lang') === currentLang;
    b.style.opacity = active ? '1' : '0.4';
    b.style.fontWeight = active ? '700' : '400';
    b.style.borderColor = active ? 'rgba(255,255,255,.5)' : 'rgba(255,255,255,.15)';
  });
  document.documentElement.lang = currentLang;
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);
  applyTranslations();
  refreshUI();
}

function refreshUI() {
  var hd = document.getElementById('headerDate');
  if (hd) hd.textContent = new Date().toLocaleDateString(getLangLocale(), {weekday: 'long', day: 'numeric', month: 'long'});
  var active = document.querySelector('.content.active');
  if (!active) return;
  var id = active.id;
  if (id === 'tab-reservations') renderReservationsTab();
  else if (id === 'tab-mesas') renderMesasOverview();
  else if (id === 'tab-articulos') { renderCatFilter(); renderArticulos(); }
  else if (id === 'tab-historial') renderHistorial();
  else if (id === 'tab-plan') { renderPlan(); renderPlanDisplay(); }
  else if (id === 'tab-usuarios') renderUsuarios();
}

applyTranslations();
